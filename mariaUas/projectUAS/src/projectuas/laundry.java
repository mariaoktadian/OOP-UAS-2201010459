    private void cboJenisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboJenisActionPerformed
      String selectedJenis = (String) cboJenis.getSelectedItem();
    if (selectedJenis.equals("Cuci Kering")) {
        txtHarga.setText("10000");
    } else if (selectedJenis.equals("Cuci Setrika")) {
        txtHarga.setText("12000");
    } else if (selectedJenis.equals("Express")) {
        txtHarga.setText("15000");
    }
    }//GEN-LAST:event_cboJenisActionPerformed

