    private void btnTransActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransActionPerformed
    if(txtNama.getText().isEmpty() || txtNo.getText().isEmpty() || txtBerat.getText().isEmpty() || cboJenis.getSelectedItem().toString().isEmpty() || txtHarga.getText().isEmpty() || txtTotal.getText().isEmpty() || txtBayar.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Data harus diisi");
    } else {
        double bayar = Double.parseDouble(txtBayar.getText());
        double totalHarga = Double.parseDouble(txtTotal.getText());
        double kembalian = bayar - totalHarga;
        JOptionPane.showMessageDialog(null, "Kembalian Anda: " + kembalian);
    }
    reset();
    dispose();
    }//GEN-LAST:event_btnTransActionPerformed

